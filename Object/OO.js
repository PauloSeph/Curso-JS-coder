// Codigo nao Executavel!!!
// Orientação object.

// Procedural
processamento(valor1, valor2, valor3)


// Quem criou o OO, orientaçao object, estava querendo passar para como se fosse na vida real. É trazera realidade do mundo, mapear
// e trazer para o software.
// OO
objeto = {
    valor1,
    valor2,
    valor3,
    processamento() {
        //...
    }
}

objeto.processamento() // Foco passou a ser o Object!

// Principios Importantes OO:  ->>> São 4!! Aula 1 object

// 1. Abstração --> É pegar um object da vida real e traduzir de como ele seria manipulado no sistema, por exemplo:
// carro, carro tem atributos, peso, largura, altura, velocidade maxima, velocidade minima, tem o metodo ligar, desligar etc
// Tem simplificar para dentro de um systema, exemplo coisas relevantes, como uma placa, quantas pessoas é o dono
// a cor, etc, coisas simples. COMO Mapear o mundo que tem seus objects e trazer para dentro do software.
// 2. Encapsulamento -->por exemplo detalhes interno do carro.
